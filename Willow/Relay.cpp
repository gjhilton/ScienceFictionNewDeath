#include "Relay.h"
#include "Arduino.h"

Relay::Relay(int _pin){
	pin = _pin;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
// ARDUINO LIFECYCLE
////////////////////////////////////////////////////////////////////////////////////////////////

void Relay::begin(){
	pinMode(pin, OUTPUT);
	off();
}

/////////////////////////////////////////////////////////////////////////////////////////////////
// PUBLIC API
////////////////////////////////////////////////////////////////////////////////////////////////

void Relay::on(){
	digitalWrite(pin, LOW);
}

void Relay::off(){
	digitalWrite(pin, HIGH);
}