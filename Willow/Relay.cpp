#include "Relay.h"
#include "Arduino.h"

Relay::Relay(int _pin){
	pin = _pin;
}

/////////////////////////////////////////////////////////////////////////////////////////////////
// ARDUINO LIFECYCLE
////////////////////////////////////////////////////////////////////////////////////////////////

void Relay::begin(){
	pinMode(pin, OUTPUT);
	off();
}

/////////////////////////////////////////////////////////////////////////////////////////////////
// PUBLIC API
////////////////////////////////////////////////////////////////////////////////////////////////

void Relay::on(){
	if (! isON){
		digitalWrite(pin, LOW);
		isON = true;
	}
}

void Relay::off(){
	digitalWrite(pin, HIGH);
	isON = false;
}